# Concept of the Day: Arrays (Memory & Complexity)
# Explanation:An Array is the most basic data structure.
# In computer memory, an array stores items in contiguous (side-by-side) memory blocks.
# Access ($O(1)$): Super fast.
# Because the computer knows exactly where the array starts and how big each item is, it can jump directly to index 100 without counting 0 to 99.
# Insertion/Deletion ($O(N)$): Slow.
# If you have an array [A, B, C, D] and you want to insert X at the beginning, you have to shift A, B, C, and D all to the right to make room.
# Key Takeaway: Use Arrays when you need fast lookups but infrequent additions/removals in the middle of the list.

