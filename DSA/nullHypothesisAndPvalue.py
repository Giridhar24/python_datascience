# Concept of the Day: The Null Hypothesis ($H_0$) & p-value
# Explanation:In science, we don't prove things true; we reject things that are false.
# Null Hypothesis ($H_0$): The boring assumption. "The drug has no effect" or "The coin is fair."
# Alternative Hypothesis ($H_1$): The exciting assumption. "The drug works" or "The coin is rigged."
# p-value: The probability that your results happened by pure luck.
# Low p-value (< 0.05):
# It's highly unlikely this was luck. We Reject the Null Hypothesis. (We found something significant!).
# High p-value (> 0.05):
# It could easily be luck. We fail to reject the Null. (Not enough evidence).
# Summary: If P is low, the Null must go.