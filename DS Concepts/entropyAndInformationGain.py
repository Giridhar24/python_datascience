# Concept of the Day: Entropy & Information Gain
# Explanation: How does a Decision Tree know which question to ask first? ("Should I check Age first or Income first?").
# It uses Entropy: A measure of "randomness" or "impurity."
# High Entropy: A mix of everything (50% Red, 50% Blue). Messy.
# Low Entropy: Pure (100% Red). Clean.
# Information Gain: The reduction in entropy. The Tree picks the question that reduces entropy the most (cleans up the data the fastest).
