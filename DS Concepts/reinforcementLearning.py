# Concept of the Day: Reinforcement Learning (RL)
#
# Explanation: Unlike Supervised Learning (Data with Answers) or Unsupervised Learning (Data without Answers), Reinforcement Learning is about Action and Reward.
#
# Agent: The learner (e.g., a Robot or Mario).
#
# Environment: The world (e.g., the maze or the game level).
#
# Action: Move Left, Jump, etc.
#
# Reward: +10 points (Good), Game Over (Bad). The Agent learns by trial and error to maximize the total reward over time.