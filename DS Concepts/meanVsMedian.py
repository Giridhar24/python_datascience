# Concept of the Day: Central Tendency (Mean vs. Median)
# Explanation: Data Science is about summarizing data. "Central Tendency" asks: "What is the 'middle' or 'typical' value?"
# 1. Mean (Average): Add all numbers and divide by the count.
# Weakness: Highly sensitive to outliers. If Bill Gates walks into a bar, the "average" wealth of everyone in the bar becomes billions, which is misleading.
# 2. Median (The Middle): Sort the data and pick the middle number.
# Strength: Robust against outliers. In the bar example, the median wealth stays roughly the same, which is a better representation of reality.
# Key Takeaway: Always check the Median if your data has extreme values (outliers).

