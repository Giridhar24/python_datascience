# Concept of the Day: Linear Regression (The Line of Best Fit)
# Explanation:Linear Regression is the "Hello World" of Machine Learning.
# It tries to draw a straight line through data points to predict a value.
# Formula: y = mx + b
# y: Prediction (e.g., House Price)
# x: Input (e.g., Square Footage)
# m: Slope (How much price goes up per sq ft)
# b: Intercept (Base price of a house)