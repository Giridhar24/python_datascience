# Concept of the Day: Logistic Regression (Classification)
# Explanation: Don't let the name confuse you. Logistic Regression is for Classification (Yes/No), not Regression (Predicting numbers).
# Linear Regression: Predicts a continuous number (e.g., Price = $500.50).
# Logistic Regression: Predicts a probability between 0 and 1, usually squashed by a "Sigmoid Function."
# Output < 0.5 -> Class 0 (Not Spam)
# Output >= 0.5 -> Class 1 (Spam)

