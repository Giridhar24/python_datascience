# Concept of the Day: ROC Curve & AUC
# Explanation: When using a classification model (e.g., Spam vs Not Spam), you can tune the "Threshold" (how strict the model is).
# ROC Curve (Receiver Operating Characteristic): A plot showing the trade-off between the True Positive Rate (Catching Spam) and False Positive Rate (False Alarms) at every possible threshold.
# AUC (Area Under the Curve): A single number score (0 to 1).
# 0.5: Random Guessing (Bad).
# 1.0: Perfect Model (Good).
# Key Takeaway: If you are comparing two models, the one with the higher AUC is generally better.

