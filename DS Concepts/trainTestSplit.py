# Concept of the Day: Train/Test Split
# Explanation: Yesterday we discussed "Overfitting." The solution is the Train/Test Split. You take your data and split it into two chunks:
# Training Set (80%): You give this to the model to learn from.
# Testing Set (20%): You hide this from the model. Once the model is trained, you test it on the hidden 20% to see if it actually learned patterns or just memorized the answers.

