# Concept of the Day: Variance & Standard Deviation

# Explanation: Yesterday we discussed the Mean (Average). But two datasets can have the same average but look very different.
# Set A: [50, 50, 50] (Mean = 50)
# Set B: [0, 50, 100] (Mean = 50)

# To describe the difference, we use Spread:

# Variance: The average squared distance from the Mean.
# Standard Deviation (Std Dev): The square root of Variance. It tells you, on average, how far data points are from the center.

# Why it matters:
# Low Std Dev: Data is clustered (Predictable).
# High Std Dev: Data is spread out (Volatile/Risky). In finance, high Std Dev = High Risk.