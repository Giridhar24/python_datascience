# Concept of the Day: K-Nearest Neighbors (KNN)
# Explanation: KNN is a simple, lazy algorithm.
# Logic: "If it walks like a duck and quacks like a duck, it's a duck."
# Algorithm:
# Pick a point you want to classify.
# Find the K closest data points to it (using distance math).
# Vote. If 3 neighbors are Blue and 1 is Red, the new point is Blue.
# K value: Usually an odd number (like 3 or 5) to avoid ties.

