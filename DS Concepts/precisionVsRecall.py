# Concept of the Day: Precision vs. Recall
# Explanation: Building on yesterday's Confusion Matrix, we have two competing metrics. Imagine you are building a Cancer Detection System.
# Precision: "Of all the people I said have cancer, how many actually do?"
# Focus: Don't scare healthy people (False Positives).
# Recall (Sensitivity): "Of all the people who actually have cancer, how many did I find?"
# Focus: Don't miss a sick person (False Negatives).
# The Trade-off: Usually, if you tune your model to catch everyone (High Recall), you will accidentally flag some healthy people (Low Precision).
# You must balance these based on the problem. (For cancer, Recall is usually more important because missing a diagnosis is fatal).

