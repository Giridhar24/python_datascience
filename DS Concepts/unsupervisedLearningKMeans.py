# Concept of the Day: Unsupervised Learning (K-Means)
# Explanation: In Supervised Learning (like Spam detection), we give the model the answers ("This is spam", "This is not").
# In Unsupervised Learning, we give the model no labels. We just say, "Here is data, find patterns."
# K-Means Clustering:
# You choose K (e.g., 3 groups).
# The algorithm randomly picks 3 centers.
# It assigns every data point to the nearest center.
# It moves the center to the middle of its new group.
# Repeat until the groups stop moving.
# Use Case: Customer Segmentation (Grouping customers by buying habits without knowing who they are).
