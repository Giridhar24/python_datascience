# Concept of the Day: Correlation (Pearson Coefficient)
# Explanation:Correlation measures the relationship between two variables.
# Positive Correlation: As X goes up, Y goes up (e.g., Height and Weight).
# Negative Correlation: As X goes up, Y goes down (e.g., Car Age and Price).
# The Coefficient ($r$): A number between -1 and 1.
# 1.0: Perfect positive match.
# 0.0: No relationship (Random).
# -1.0: Perfect negative match.
# Warning: "Correlation does not imply Causation."
# Just because Ice Cream sales and Shark attacks both go up in summer (High Correlation) doesn't mean Ice Cream attracts Sharks. (The cause is Summer/Heat).

